export function encodeIdentifier(id: string) {
  const r = /[^._a-zA-Z0-9]/gim;
  id = id.replace(r, (s) => s.charCodeAt(0).toString(16));
  if (JS_RESERVED.includes(id) || id.match(/^\d/)) {
    id = "_" + id;
  }
  return id;
}
